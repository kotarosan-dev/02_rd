def main():
    for fname, title, body in BLOCKS:
        p = HERE / fname
        if not p.exists():
            continue
        chunk = f"\n\n---\n\n{title}\n\n{body}\n"
        text = p.read_text(encoding="utf-8").rstrip() + chunk
        p.write_text(text + "\n", encoding="utf-8")
    print("appended", len(BLOCKS), "sections")

if __name__ == "__main__":
    main()
